package entity;

import java.util.UUID;

/**
 * Класс User представляет пользователя с уникальным идентификатором (UUID).
 *
 * <p>Поддерживает автоматическую генерацию уникального идентификатора
 * при создании нового объекта или установку идентификатора из внешнего источника
 * (например, при загрузке из базы данных).</p>
 */
public class User {
    /**
     * Уникальный идентификатор пользователя.
     * <p>Генерируется автоматически при создании нового объекта, если не указан явно.</p>
     */
    private UUID id;

    /**
     * Конструктор по умолчанию.
     * <p>Создает пользователя с автоматически сгенерированным UUID.</p>
     */
    public User() {
        this.id = UUID.randomUUID();
    }

    /**
     * Конструктор с параметром.
     * <p>Позволяет установить UUID для объекта, например, при загрузке данных из базы.</p>
     *
     * @param id уникальный идентификатор пользователя.
     */
    public User(UUID id) {
        this.id = id;
    }

    /**
     * Возвращает уникальный идентификатор пользователя.
     *
     * @return уникальный идентификатор (UUID) пользователя.
     */
    public UUID getId() {
        return id;
    }

    /**
     * Устанавливает уникальный идентификатор пользователя.
     * <p>Может использоваться для обновления идентификатора, например, при загрузке из базы данных.</p>
     *
     * @param id новый уникальный идентификатор пользователя.
     */
    public void setId(UUID id) {
        this.id = id;
    }
}

